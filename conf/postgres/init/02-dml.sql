-- 信用卡當月額度上限測試資料 DML
-- 隨機產生一批測試卡號與額度上限，涵蓋規則引擎所有分級區間：
--   <= 100000 / (100000, 500000] / (500000, 1000000] / > 1000000
-- ============================================================================

-- 1. 隨機批量產生 30 筆測試卡號 (CARD000001 ~ CARD000030)
INSERT INTO card_limit (card_number, credit_limit)
SELECT
    'CARD' || LPAD(gs::text, 6, '0'),
    (ARRAY[50000, 80000, 100000, 150000, 300000, 450000, 500000,
           600000, 750000, 800000, 1000000, 1200000, 1500000])
        [1 + floor(random() * 13)::int]
FROM generate_series(1, 30) AS gs
ON CONFLICT (card_number) DO NOTHING;

-- 2. 對應 README 範例交易資料（cardNumber: "****1234"）之固定測試卡號，
--    方便直接用既有範例資料驗證規則引擎（涵蓋 4 種區間各一筆）
INSERT INTO card_limit (card_number, credit_limit) VALUES
    ('****1234', 90000.00),
    ('****5678', 300000.00),
    ('****9999', 800000.00),
    ('****0000', 1200000.00)
ON CONFLICT (card_number) DO NOTHING;
